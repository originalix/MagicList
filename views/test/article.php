        <div class="l-main-content">
            <p>目前山东鲁能正在广州进行冬训，与此同时，就在昨天下午，K联赛全北现代体能教练法比奥与全北球迷告别，他将带着荣誉离开韩国，巴西人的下一站是山东鲁能，他将出任鲁能第一助教。同时有消息称，除法比奥外，鲁能教练组可能加入另一韩国元素，知名球员李同国可能随法比奥一同加入山东鲁能教练组，担任专项前场进攻教练。</p>

            <p>不过，就在之前不久，也就是大约半月前，韩国全北现代俱乐部官方宣布与老将李同国续约一年，自2009年加盟全北现代以来，随队获得5个联赛冠军和1个亚冠冠军，今年他代表全北现代在K联赛打入10球，连续9个赛季进球上双。此番能否加盟鲁能还有一定疑问，毕竟如果加盟鲁能教练组，专门负责前锋线的训练，那么他将与全北现代毁约，也就是宣布正式退役，当然，李同国现在毕竟已经38岁，转型也是他考虑的问题之一。</p>

            <p>同时在外籍教练组中，还有一名守门员教练，那就是卢卡斯，他曾经在青岛中能时期与李霄鹏一起共事过，他1981年12月12日出生于巴西，今年才36岁，2013年，卢卡斯加盟青岛中能，后来他转投重庆力帆，追随韩国教练张外龙，此番算是再次回到山东工作。</p>

            <p>也有巴西媒体报道，正在度假的中超鲁能外援塔尔德利确认，他将与鲁能续约三年。此前确有巴西多家媒体曾报道塔尔德利将在2018赛季返回巴甲踢球。</p>

            <p>目前可以确定，在新赛季冬训拉练刚刚开始的时候，鲁能下赛季外援调整刚刚开始列入日程，而现阶段球队搭建新教练组和制定拉练计划是首要之事，按照球队计划，此次广州拉练结束前，新赛季的教练班底就会完全敲定。
